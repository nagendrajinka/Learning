package training.weekOne;

/**
 * Program to display first ten natural numbers in reverse order using FOR loop
 */

public class FirstTenNaturalInReverseOrderFor {
    public static void main(String[] args){
        for(int x=10;x>=1;x--){
            System.out.println("x="+x);
            System.out.println("\n");
        }
    }
}
