package training.weekOne;

/**
 * Program to display first ten natural numbers in reverse order using WHILE loop
 */

public class FirstTenNaturalInReverseOrder {
    public static void main(String[] args){
        int x=10;
        while(x>=1){
            System.out.println("x="+x);
            x--;
            System.out.println("\n");
        }
    }
}
