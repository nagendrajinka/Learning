package training.weekOne;

/**
 * Program to display first ten natural numbers using WHILE loop
 */

public class FirstTenNaturalWhile {
    public static void main(String[] args){
        int x=1;
        while(x<=10){
            System.out.println("x="+x);
            x++;
            System.out.println("\n");
        }
    }
}
